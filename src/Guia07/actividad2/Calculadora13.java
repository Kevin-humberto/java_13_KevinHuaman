    private void jButton25ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton25ActionPerformed
        this.segundoNumero=Float.parseFloat(this.Mostrar.getText());
        switch (this.operador) {
            case "+":this.Mostrar.setText(sinCero(this.primerNumero+this.segundoNumero));break;
            case "-":this.Mostrar.setText(sinCero(this.primerNumero-this.segundoNumero));break;
            case "*":this.Mostrar.setText(sinCero(this.primerNumero*this.segundoNumero));break;
            case "/":if (this.segundoNumero==0){this.Mostrar.setText("No se puede dividir entre 0");}
            else{
                this.Mostrar.setText(sinCero(this.primerNumero/this.segundoNumero));}break;
        }
    }//GEN-LAST:event_jButton25ActionPerformed

